// 카테고리별 필터링 함수
export const getPostsByCategory = (category, subcategory = null) => {
  return postsData.filter(post => {
    if (subcategory) {
      return post.category === category && post.subcategory === subcategory;
    }
    return post.category === category;
  });
};

// 인기 포스트 가져오기
export const getFeaturedPosts = () => {
  return postsData.filter(post => post.featured && post.status === 'published');
};

// 최신 포스트 가져오기
export const getRecentPosts = (limit = 5) => {
  return postsData
    .filter(post => post.status === 'published')
    .sort((a, b) => new Date(b.dates.published) - new Date(a.dates.published))
    .slice(0, limit);
};

// 태그별 포스트 가져오기
export const getPostsByTag = (tag) => {
  return postsData.filter(post => 
    post.metadata.tags.includes(tag) && post.status === 'published'
  );
};

// 검색 함수
export const searchPosts = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return postsData.filter(post => {
    const searchableText = `
      ${post.title} 
      ${post.metadata.summary} 
      ${post.metadata.tags.join(' ')} 
      ${post.content.excerpt}
    `.toLowerCase();
    
    return searchableText.includes(lowercaseQuery) && post.status === 'published';
  });
};